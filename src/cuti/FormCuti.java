    private void initRowTableCuti() {
       tableModelCuti.getDataVector().removeAllElements();
       tableModelCuti.fireTableDataChanged();
       try {
            List<Cuti> dataCuti = cutiController.dataCuti();
            
            
            dataCuti.stream().map((item) -> {
                Object[] obj = new Object[6];
                Karyawan karyawan = karyawanController.detailKaryawan(item.idKaryawan);
                obj[0] = item.id;
                obj[1] = item.idKaryawan;
                obj[2] = karyawan.nama;
                obj[3] = utils.dMY(utils.toDate(item.tanggal));
                obj[4] = item.jumlahHari;
                obj[5] = item.keterangan;
               return obj;
           }).forEachOrdered((obj) -> { 
               tableModelCuti.addRow(obj);
           });
           
       } catch(Exception ex) {
           System.out.println("initRowTableCuti ERROR" + ex);
       }
    }
    
    private void initRowTableKaryawan() {
       tableModelKaryawan.getDataVector().removeAllElements();
       tableModelKaryawan.fireTableDataChanged();
       try {
            List<Karyawan> karyawan = karyawanController.dataKaryawan(false);
            
            
            karyawan.stream().map((item) -> {
                Object[] obj = new Object[4];
                Jabatan jabatan = jabatanController.detailJabatan(item.idJabatan);
                obj[0] = item.id;
                obj[1] = item.nama;
                obj[2] = jabatan.namaJabatan;
               return obj;
           }).forEachOrdered((obj) -> { 
               tableModelKaryawan.addRow(obj);
           });
           
       } catch(Exception ex) {
           System.out.println("initRowTableKaryawan ERROR" + ex);
       }
    }
    
