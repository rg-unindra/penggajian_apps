    private void tbl_cutiMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_cutiMousePressed
       try {
            int index = tbl_cuti.getSelectedRow();
            TableModel mdl = tbl_cuti.getModel();
            String id = mdl.getValueAt(index, 0).toString();
            Cuti cuti = cutiController.detailCuti(id);
            String idKaryawan = mdl.getValueAt(index, 1).toString();
            String namaKaryawan = mdl.getValueAt(index, 2).toString();
            String jumlahHari = mdl.getValueAt(index, 4).toString();
           
            txt_id_karyawan.setText(idKaryawan);
            txt_nama_karyawan.setText(namaKaryawan);
            spn_tanggal.setDate(utils.toDate(cuti.tanggal));
            spn_hari.setValue(Integer.parseInt(jumlahHari));
            txt_keterangan.setText(cuti.keterangan);
            selectedId = cuti.id;
       } catch(Exception ex) {
            System.out.println(ex);
       }
    }//GEN-LAST:event_tbl_cutiMousePressed

