    private void btn_tambahMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_tambahMousePressed
       try {
            String id = txt_id.getText();
            String nama = txt_nama.getText();
            long gapok = Long.parseLong(txt_gapok.getText());
            long tunjangan = Long.parseLong(txt_tunjangan.getText());
            
            boolean tambah =  jabatanController.tambah(id, nama, gapok, tunjangan);
            
            if(tambah) {
                JOptionPane.showMessageDialog(this, "Data jabatan berhasil ditambahkan!");
                initRowTabel();
                resetForm();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambah data jabatan");
            }
           
       } catch(HeadlessException | NumberFormatException ex) {
            System.out.println("Buton Tambah Exception => " + ex);
       }
    }//GEN-LAST:event_btn_tambahMousePressed

