    private void tbl_jabatanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_jabatanMouseClicked
        int index = tbl_jabatan.getSelectedRow();
        TableModel mdl = tbl_jabatan.getModel();
        String id = mdl.getValueAt(index, 0).toString();
        String nama =  mdl.getValueAt(index, 1).toString();
        String gapok = mdl.getValueAt(index, 2).toString();
        String tunjangan = mdl.getValueAt(index, 3).toString();
        
        
        txt_id.setEditable(false);
        txt_id.setText(id);
        txt_nama.setText(nama);
        txt_gapok.setText(gapok);
        txt_tunjangan.setText(tunjangan);
    }//GEN-LAST:event_tbl_jabatanMouseClicked

