        private void initColumnTabel() {
        model = new DefaultTableModel ();
        
        tbl_jabatan.setModel(model);
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Gaji Pokok");
        model.addColumn("Tunjangan");
    }
        
    private void initRowTabel() {
       model.getDataVector().removeAllElements();
       model.fireTableDataChanged();
       try {
            List<Jabatan> jabatan = jabatanController.dataJabatan();
            
            
            for(int i = 0; i < jabatan.size(); i++) {
               Object[] obj = new Object[4];
               Jabatan item = jabatan.get(i);
               obj[0] = item.id;
               obj[1] = item.namaJabatan;
               obj[2] = item.gajiPokok;
               obj[3] = item.tunjangan;
               model.addRow(obj); 
            } 
           
       } catch(Exception ex) {
           System.out.println("initRowTabel ERROR" + ex);
       }
    }
    
    private void resetForm() {
        txt_id.setEditable(true);
        txt_id.setText("");
        txt_nama.setText("");
        txt_gapok.setText("");
        txt_tunjangan.setText("");
    }
    
    

